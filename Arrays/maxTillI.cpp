//given a array a[] of size n .for every i from 0-n-1  output max a[] time complexity O(n)
#include<iostream>
using namespace std;
int main()
{
    int mx=-1999999;
    int n;
    cout<<"Enter the size of array\n";
    cin>>n;
    int a[n];
    for (int i = 0; i < n ; i++)
    {
        cout<<"Enter the elements\n";
        cin>>a[i];
    }
    
    for(int i=0;i<n;i++){
        mx=max(mx,a[i]);
        cout<<mx<<endl;
    }
    
    return 0;
}